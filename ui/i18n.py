class LocaleManager(QObject):
    """Singleton that manages the current locale and provides translations."""

    locale_changed = Signal(str)

    _instance: "LocaleManager | None" = None

    def __init__(self, parent: QObject | None = None) -> None:
        if LocaleManager._instance is not None:
            raise RuntimeError("Use locale_manager() to get the LocaleManager singleton.")
        super().__init__(parent)
        self._current_locale: str = "en"
        self._settings_repository: Any = None
        LocaleManager._instance = self

    @property
    def current_locale(self) -> str:
        return self._current_locale

    def initialize(self, settings_repository: Any) -> None:
        """Load persisted locale from the settings table (called once at startup)."""
        self._settings_repository = settings_repository
        saved = settings_repository.get("app_locale", "en").strip()
        if saved in ("en", "zh"):
            self._current_locale = saved

    def tr(self, key: str, **kwargs: object) -> str:
        """Return the translated string for *key*, formatted with *kwargs*."""
        translations = _ZH if self._current_locale == "zh" else _EN
        template: str = translations.get(key) or _EN.get(key, key)
        if kwargs:
            try:
                return template.format(**kwargs)
            except (KeyError, ValueError):
                return template
        return template

    def set_locale(self, locale: str) -> None:
        """Switch language, persist, and emit locale_changed."""
        if locale not in ("en", "zh"):
            raise ValueError(f"Unsupported locale: {locale}")
        if locale == self._current_locale:
            return
        self._current_locale = locale
        if self._settings_repository is not None:
            self._settings_repository.set("app_locale", locale)
        self.locale_changed.emit(locale)


def locale_manager() -> LocaleManager:
    """Return the global LocaleManager singleton."""
    if LocaleManager._instance is None:
        LocaleManager()
    instance = LocaleManager._instance
    assert instance is not None
    return instance


def tr(key: str, **kwargs: object) -> str:
    """Convenience shorthand for ``locale_manager().tr(key, **kwargs)``."""
    return locale_manager().tr(key, **kwargs)
